import { findContentById } from '@src/core/utils/finder'
import _ from 'lodash'
import { baseStatsObject, getPlungeScaling, StatsObject } from '../../baseConstant'
import { Element, Stats, TalentProperty } from '@src/domain/genshin/constant'
import { StatObjectT } from '@src/core/hooks/useStat'
import { toPercentage } from '@src/core/utils/converter'
import { IContent, ITalent } from '@src/domain/genshin/conditional'
import { calcScaling } from '../../../../../core/utils/data_format'

const Albedo = (c: number, a: number, stat: StatObjectT) => {
  const maxFatalReckoning = 4

  const talents: ITalent = {
    normal: {
      title: 'Favonius Bladework - Weiss',
      content: `<b>Normal Attack</b>
      <br />Performs up to 5 rapid strikes.
      <br />
      <br /><b>Charged Attack</b>
      <br />Consumes a certain amount of Stamina to unleash 2 rapid sword strikes.
      <br />
      <br /><b>Plunging Attack</b>
      <br />Plunges from mid-air to strike the ground below, damaging opponents along the path and dealing AoE DMG upon impact.
      `,
    },
    skill: {
      title: 'Abiogenesis: Solar Isotoma',
      content: `Albedo creates a Solar Isotoma using alchemy, which deals <b class="text-genshin-geo">AoE Geo DMG</b> on appearance.
      <br />
      <br /><b>Solar Isotoma</b>
      <br />has the following properties:
      <br />- When opponents within the Solar Isotoma field take DMG, the Solar Isotoma will generate Transient Blossoms which deal <b class="text-genshin-geo">AoE Geo DMG</b>. DMG dealt scales off Albedo's DEF.
      <br />- Transient Blossoms can only be generated once every <span class="text-yellow">2</span>s.
      <br />- When a character is located at the locus of the Solar Isotoma, the Solar Isotoma will accumulate Geo power to form a crystallized platform that lifts the character up to a certain height. Only one crystallized platform can exist at a time.
      <br />- Solar Isotoma is considered a <b class="text-genshin-geo">Geo construct</b>. Only one Solar Isotoma created by Albedo himself can exist at a time.
      <br />
      <br /><b>Hold</b> to designate the location of the skill.`,
    },
    burst: {
      title: 'Rite of Progeniture: Tectonic Tide',
      content: `Under Albedo's command, Geo crystals surge and burst forth, dealing <b class="text-genshin-geo">AoE Geo DMG</b> in front of him.
      <br />If a Solar Isotoma created by Albedo himself is on the field, 7 Fatal Blossoms will be generated in the Solar Isotoma field, bursting violently into bloom and dealing <b class="text-genshin-geo">AoE Geo DMG</b>.
      <br />
      <br />Tectonic Tide DMG and Fatal Blossom DMG will not generate Transient Blossoms.`,
    },
    a1: {
      title: 'A1: Calcite Might',
      content: `Transient Blossoms generated by Abiogenesis: Solar Isotoma deal <span class="text-yellow">25%</span> more DMG to opponents whose HP is below <span class="text-yellow">50%</span>.`,
    },
    a4: {
      title: 'A4: Homuncular Nature',
      content: `Using Rite of Progeniture: Tectonic Tide increases the Elemental Mastery of nearby party members by <span class="text-yellow">125</span> for <span class="text-yellow">10</span>s.`,
    },
    c2: {
      title: 'C2: Flower of Eden',
      content: `Transient Blossoms generated by Abiogenesis: Solar Isotoma grant Albedo Fatal Reckoning for <span class="text-yellow">30</span>s:
      <br />- Unleashing Rite of Progeniture: Tectonic Tide consumes all stacks of Fatal Reckoning. Each stack of Fatal Reckoning consumed increases the DMG dealt by Fatal Blossoms and Rite of Progeniture: Tectonic Tide's burst DMG by <span class="text-yellow">30%</span> of Albedo's DEF.
      <br />- This effect stacks up to <span class="text-yellow">4</span> times.`,
    },
    c4: {
      title: 'C4: Descent of Divinity',
      content: `Active party members within the Solar Isotoma field have their Plunging Attack DMG increased by <span class="text-yellow">30%</span>.`,
    },
    c6: {
      title: 'C6: Dust of Purification',
      content: `Active party members within the Solar Isotoma field who are protected by a shield created by Crystallize have their DMG increased by <span class="text-yellow">17%</span>.`,
    },
  }

  const content: IContent[] = [
    {
      type: 'toggle',
      id: 'calciteMight',
      text: `Calcite Might`,
      ...talents.a1,
      show: a >= 1,
      value: [{ name: 'Transient Blossoms DMG', value: 0.25, formatter: toPercentage }],
      default: true,
    },
    {
      type: 'toggle',
      id: 'homuncularNature',
      text: `Homuncular Nature`,
      ...talents.a4,
      show: a >= 4,
      value: [{ name: Stats.EM, value: 125, formatter: _.toString }],
      default: true,
    },
    {
      type: 'number',
      id: 'fatalReckoningStacks',
      text: `Fatal Reckoning Stacks`,
      ...talents.c2,
      show: c >= 2,
      value: [{ name: 'Elemental Burst DMG', value: 0.3 * stat.def, formatter: _.floor }],
      min: 0,
      max: maxFatalReckoning,
      default: maxFatalReckoning,
    },
    {
      type: 'toggle',
      id: 'descentOfDivinity',
      text: `Descent of Divinity`,
      ...talents.c4,
      show: c >= 4,
      value: [{ name: 'Plunge DMG%', value: 0.3, formatter: toPercentage }],
      default: true,
    },
    {
      type: 'toggle',
      id: 'dustOfPurification',
      text: `Dust of Purification`,
      ...talents.c6,
      show: c >= 6,
      value: [{ name: Stats.ALL_DMG, value: 0.17, formatter: toPercentage }],
      default: true,
    },
  ]

  const teammateContent: IContent[] = [
    findContentById(content, 'homuncularNature'),
    findContentById(content, 'descentOfDivinity'),
    findContentById(content, 'dustOfPurification'),
  ]

  return {
    talents,
    content,
    teammateContent,
    preCompute: (form: Record<string, any>) => {
      const base = _.cloneDeep(baseStatsObject)

      const fatalReckoningScaling =
        form.fatalReckoningStacks > 0 ? [{ scaling: 0.3 * form.fatalReckoningStacks, multiplier: Stats.DEF }] : []

      base.BASIC_SCALING = [
        {
          name: '1-Hit',
          value: [{ scaling: calcScaling(0.3674, 10, 'physical', '1'), multiplier: Stats.ATK }],
          element: Element.PHYSICAL,
          property: TalentProperty.NA,
        },
        {
          name: '2-Hit',
          value: [{ scaling: calcScaling(0.3674, 10, 'physical', '1'), multiplier: Stats.ATK }],
          element: Element.PHYSICAL,
          property: TalentProperty.NA,
        },
        {
          name: '3-Hit',
          value: [{ scaling: calcScaling(0.4745, 10, 'physical', '1'), multiplier: Stats.ATK }],
          element: Element.PHYSICAL,
          property: TalentProperty.NA,
        },
        {
          name: '4-Hit',
          value: [{ scaling: calcScaling(0.4975, 10, 'physical', '1'), multiplier: Stats.ATK }],
          element: Element.PHYSICAL,
          property: TalentProperty.NA,
        },
        {
          name: '5-Hit',
          value: [{ scaling: calcScaling(0.6207, 10, 'physical', '1'), multiplier: Stats.ATK }],
          element: Element.PHYSICAL,
          property: TalentProperty.NA,
        },
      ]
      base.CHARGE_SCALING = [
        {
          name: '1-Hit',
          value: [{ scaling: calcScaling(0.473, 10, 'physical', '1'), multiplier: Stats.ATK }],
          element: Element.PHYSICAL,
          property: TalentProperty.CA,
        },
        {
          name: '2-Hit',
          value: [{ scaling: calcScaling(0.602, 10, 'physical', '1'), multiplier: Stats.ATK }],
          element: Element.PHYSICAL,
          property: TalentProperty.CA,
        },
      ]
      base.PLUNGE_SCALING = getPlungeScaling('base')
      base.SKILL_SCALING = [
        {
          name: 'Skill DMG',
          value: [{ scaling: calcScaling(1.304, 10, 'elemental', '1'), multiplier: Stats.ATK }],
          element: Element.GEO,
          property: TalentProperty.SKILL,
        },
        {
          name: 'Transient Blossom DMG',
          value: [{ scaling: calcScaling(1.336, 10, 'elemental', '1'), multiplier: Stats.DEF }],
          element: Element.GEO,
          property: TalentProperty.SKILL,
          bonus: form.calciteMight ? 0.25 : null,
        },
      ]
      base.BURST_SCALING = [
        {
          name: 'Skill DMG',
          value: [{ scaling: calcScaling(3.672, 10, 'elemental', '1'), multiplier: Stats.ATK }],
          element: Element.GEO,
          property: TalentProperty.BURST,
        },
        {
          name: 'Fatal Blossom DMG [x7]',
          value: [
            { scaling: calcScaling(0.72, 10, 'elemental', '1'), multiplier: Stats.DEF },
            ...fatalReckoningScaling,
          ],
          element: Element.GEO,
          property: TalentProperty.BURST,
        },
      ]

      if (form.dustOfPurification) base[Stats.ALL_DMG] += 0.17
      if (form.homuncularNature) base[Stats.EM] += 125
      if (form.descentOfDivinity) base.PLUNGE_DMG += 0.3

      return base
    },
    preComputeShared: (base: StatsObject, form: Record<string, any>) => {
      if (form.dustOfPurification) base[Stats.ALL_DMG] += 0.17
      if (form.homuncularNature) base[Stats.EM] += 250
      if (form.descentOfDivinity) base.PLUNGE_DMG += 0.3

      return base
    },
  }
}

export default Albedo
